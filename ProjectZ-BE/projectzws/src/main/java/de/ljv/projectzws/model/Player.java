package de.ljv.projectzws.model;

public class Player {
    
    String id;
    String name;
    String avatarId;
    
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getAvatarId() {
        return avatarId;
    }
    public void setAvatarId(String avatarId) {
        this.avatarId = avatarId;
    }
    
}
